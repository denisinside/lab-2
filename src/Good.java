public class Good {
    
    // Object fields
    String name;
    String description;
    String producer;
    int  price;
    int amount;
    short groupNum;

    public Good(String name, String description, String producer, int price, int amount, short groupNum) {
        this.name = name;
        this.description = description;
        this.producer = producer;
        this.price = price;
        this.amount = amount;
        this.groupNum = groupNum;
    }
}
